time=linspace(0,0.1,4000);
y=subs(var,t,time);
y=double(y);
%whitebg([1 1 1])
[s,f,t,p]=spectrogram(y,128,120,128,(max(time)-min(time))\(length(time)));
surf(f,t,p.')
xlabel('Freq (Hz)','FontSize',14)
ylabel('Time (s)','FontSize',14)
zlabel('Power','FontSize',14)
shading interp
view(-20,50)
colorbar off
handles.time=time;
handles.sig=y;
